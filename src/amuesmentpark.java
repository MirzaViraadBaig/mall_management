    private void addActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addActionPerformed
        // TODO add your handling code here:
        rideName = ridenameText.getText();
        ticketPrice = Double.parseDouble(ticketpriceText.getText());
        capacity = Integer.parseInt(capacityText.getText());
        rideList.add(new amuesmentparkClass(rideName,ticketPrice,capacity));
        dtm.setRowCount(0);
        for(int i = 0; i < rideList.size(); i++){
        Object [] objs = {rideList.get(i).rideName, rideList.get(i).ticketPrice,rideList.get(i).capacity};
        dtm.addRow(objs);
        }
        clearField();
    }//GEN-LAST:event_addActionPerformed
