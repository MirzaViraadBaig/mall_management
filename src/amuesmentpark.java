    private void saveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveActionPerformed
        if(dtm.getRowCount() == 0){
            JOptionPane.showMessageDialog(this,"The Table is Empty");
        }
        else{
            String ride_name;
            double ticket_price;
            int capacity;
            for(int i =0 ; i<dtm.getRowCount(); i++){
                try {
                    rideName = dtm.getValueAt(i, 0).toString();
                    ticket_price = Double.parseDouble(dtm.getValueAt(i,1).toString());
                    capacity = Integer.parseInt(dtm.getValueAt(i,2).toString());
                    
                    String query = "insert into ride(ride_name,ticket_price,capacity) values(?,?,?)";
                    db.ps = db.con.prepareStatement(query);
                    PreparedStatement prepstmt;
                    prepstmt = db.con.prepareStatement(query);



                  prepstmt.setString(1, rideName);
                  prepstmt.setDouble(2,ticket_price);
                  prepstmt.setInt(3,capacity);

                  
 prepstmt.executeUpdate();
                } catch (SQLException ex) {
                    Logger.getLogger(amuesmentpark.class.getName()).log(Level.SEVERE, null, ex);
                }
                }
                JOptionPane.showMessageDialog(this,"Added Successfully");
               //tm.setRowCount(0);
                
            } 
            
                
    }//GEN-LAST:event_saveActionPerformed

