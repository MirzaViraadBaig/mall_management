    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
        row = jTable1.getSelectedRow();
        column = jTable1.getSelectedColumn();
        System.out.println(row+ "" + column);
        sn.setText(dtm.getValueAt(row, 0).toString());
        on.setText(dtm.getValueAt(row, 1).toString());
        floortext.setText(dtm.getValueAt(row,3).toString());
        String loc = dtm.getValueAt(row, 2).toString();
        for(int i = 0 ; i<ctgry.getItemCount();i++){
            if(ctgry.getItemAt(i).equalsIgnoreCase(loc)){
                ctgry.setSelectedIndex(i);
            }
        }
    }//GEN-LAST:event_jTable1MouseClicked

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        try {
            // TODO add your handling code here:
            rent r = new rent();
            r.setVisible(true);
            this.setVisible(false);
            db.st = db.con.createStatement();
            db.rs = db.st.executeQuery("select shop_name , owner_name from shop");
            while(db.rs.next()){
                String name = db.rs.getString("shop_name");
                String owner_name = db.rs.getString("owner_name");
                String info = name+ ","+ owner_name;
                r.combo.addItem(info);
            }
        } catch (SQLException ex) {
            Logger.getLogger(shopmenu.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_jButton2ActionPerformed

