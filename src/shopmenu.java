    private void removeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_removeActionPerformed
        // TODO add your handling code here:
        int diaglogButton = JOptionPane.YES_NO_OPTION;
        int diaglogResult = JOptionPane.showConfirmDialog(this, "Delete this data", "Delete", diaglogButton);
        if(diaglogResult == 0){
            dtm.removeRow(row);
            shopList.remove(row);
            dtm.setRowCount(0);
            for(int i = 0 ; i<shopList.size();i++){
                Object [] objs = {shopList.get(i).shopName, shopList.get(i).ownerName, shopList.get(i).shopCategory , shopList.get(i).floor};
                dtm.addRow(objs);
                
            }
    }//GEN-LAST:event_removeActionPerformed
    }
