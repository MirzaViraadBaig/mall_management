    private void addActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addActionPerformed
        // TODO add your handling code here:
         shopName = sn.getText();
         ownername = on.getText();
         //shopCategory = ctgry.getSelectedItem().toString();
         floor = Integer.parseInt(floortext.getText());
        shopList.add(new shopclass(shopName,ownername,shopCategory,floor));
        dtm.setRowCount(0);
        for(int i = 0; i < shopList.size(); i++){
            Object [] objs = {shopList.get(i).shopName, shopList.get(i).ownerName, shopList.get(i).shopCategory , shopList.get(i).floor , shopList.get(i).rent};
            dtm.addRow(objs);
            
        }
        clearField();
        
    }//GEN-LAST:event_addActionPerformed
